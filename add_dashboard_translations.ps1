foreach ($lang in $dashboardTranslations.Keys) {
    $file = "public\locales\$lang.json"
    $content = Get-Content $file -Raw
    
    # Find the position before the last closing brace
    $lastBracePos = $content.LastIndexOf('}')
    $beforeLastBrace = $content.Substring(0, $lastBracePos).TrimEnd()
    
    # Add comma if needed
    if (-not $beforeLastBrace.EndsWith(',')) {
        $beforeLastBrace += ','
    }
    
    # Insert dashboard section
    $newContent = $beforeLastBrace + "`r`n" + $dashboardTranslations[$lang] + "`r`n}"
    
    $newContent | Set-Content $file -NoNewline
    Write-Host "Updated $file"
}

Write-Host "Dashboard translations added to all locale files!"
